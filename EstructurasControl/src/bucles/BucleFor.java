package bucles;

public class BucleFor {
	
	public static void main(String[] args) {
		
		for (int i=1;i<=10;i++) {
			
			System.out.println(i);
		}
		
		System.out.println("------------------------");
		
		for (int u=10;u>=1;u--) 
		{
			
			System.out.println(u);
		}
		
		System.out.println("-------------------------");
		
		for (int numpar=2; numpar<=10;numpar=numpar+2) 
		{
			
			System.out.println(numpar);
		}
		
		System.out.println("-------------------------");
		
		
	}

}
